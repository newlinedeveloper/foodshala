        success : function(result){
          var obj = JSON.parse(result);
          var card = "";

          obj.forEach( res => {
            console.log(res);

            card += '<div class="col-lg-4 mt-4 mb-2 mt-lg-0">';
            card += '<div class="box">';
            card += '<span>'+res.menu_name+'</span>';
            card += '<h4>'+res.category+'</h4>';
            card += '<p>'+res.description+'</p>';
            card += '</div>';
            card += '</div>';
            card +='';


          });

          

          $("#menu_list").html(card);
        }

      });

